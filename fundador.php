					<p class="lead text-justify" style="color:#6C7A89;">
						Fundador de la FMC nació en Bélgica el 18 de julio de 1913 y murió en Manizales Colombia el 7 de marzo de 1992.
						<br>
						En 1940 se ordenó como sacerdote asuncionista y desde su postulado como religioso asuncionista se preocupó por las almas abandonados de Dios. Pensó en fundar un movimiento de evangelizadores en Bélgica, pero fué enviado por su superior a Colombia donde había empezado la fundación de una casa asuncionista en Cali.
						<br><br>
						Preocupado por la discriminación europea dudó en la aceptación de la propuesta de su superior. Se fue en peregrinación de consulta al Santuario de la Virgen de Kortenbos. Una vez allí, de rodillas frente a la pequeña imagen, había rezado unas pocas ave marías, cuando pasó por su mente en forma muy clara esta idea; "Lo que no puedes hacer aquí lo podrás hacer allá".
						<br><br>
						Ya en el convento escribió la carta de aceptación de su nombramiento para Colombia. El 4 de marzo de 1947 llegó a Barranquilla para seguir a Cali. El 11 de febrero de 1948 fue trasladado para Bogotá con el fin de fundar una casa asuncionista. Perdió todas sus pertenencias el 9 de abril de 1948, con el bogotazo.
						<br><br>
						Una vez de párroco de Santa Sofía conoció a María Teresa Peralta con quién dió los primeros pasos para la fundación de la FMC. Siguieron 7 años de oración, preparación y lucha para ya iniciar concretamente la fundación.
						<br><br>
						Durante 37 años impulsó con gran tesón el desarrollo de la FMC dejando como herencia un movimiento evangelizador para la Iglesia.
						<br><br>
						Su vida entera fue portadora de un mensaje de entraga a Dios, de fe y Evangelización. Se destacaba como un apóstol entusiasta, alegre y emprendedor. Su manera de responder a Dios y de amar a la Iglesia por encima de todas las fragilidades significa un testimonio imborrable.
